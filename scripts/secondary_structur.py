def compare_strings(string1, string2):
    """
    Vergleicht zwei Strings und gibt die unterschiedlichen Zeichen sowie deren Positionen aus.

    :param string1: Der erste zu vergleichende String
    :param string2: Der zweite zu vergleichende String
    """
    # Bestimmen der maximalen Länge beider Strings
    max_len = max(len(string1), len(string2))
    
    # Initialisieren einer Liste für die Unterschiede
    differences = []

    # Durchlaufen der Zeichen beider Strings
    for i in range(max_len):
        char1 = string1[i] if i < len(string1) else None
        char2 = string2[i] if i < len(string2) else None

        if char1 != char2:
            differences.append((i, char1, char2))
    
    # Drucken der Unterschiede
    for position, char1, char2 in differences:
        print(f"Position {position}: {char1} != {char2}")

